    // Pesquisa produto por cod_produto
    private void btnProdPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProdPesquisarActionPerformed
        // Verifica se o código é válido (numérico, positivo e inteiro)
        if (verifaCampos(txtProdCod) == true){
            // Converte o cod de texto para inteiro
            int prodCod = Integer.parseInt(txtProdCod.getText());
            
            // Instancia a classe Produto_controle
            Produto_controle produto_controle = new Produto_controle();
            
            // Chama método que busca um produto com um cod específico
            Produto_modelo produto = produto_controle.buscarProdutoPorCod(prodCod);
            
            // Insere as informações do produto pesquisado nos campos correspondentes do formulário
            txtProdNome.setText(produto.getNome());
            txaProdDesc.setText(produto.getDescrição());
            txtProdValCom.setText(String.valueOf(produto.getValor_compra()));
            txtProdValVen.setText(String.valueOf(produto.getValor_venda()));
            txtProdQuant.setText(String.valueOf(produto.getQuantidade()));
            txtProdTipo.setText(produto.getTipo());
            txtProdDataCad.setText(String.valueOf(produto.getDatacad()));
            txtProdHoraCad.setText(String.valueOf(produto.getHoracad()));
            txtProdQuantMin.setText(String.valueOf(produto.getquantidadeMin()));
        } else {
            JOptionPane.showMessageDialog(null, "Informe um valor positivo e inteiro.");
        }
    }//GEN-LAST:event_btnProdPesquisarActionPerformed

    // Verifica se o campos de texto são númericos, inteiros e positivos
    public boolean verifaCampos(JTextField campo){
        try {
            String pegaCampo = campo.getText();
            int converteCampo = Integer.parseInt(pegaCampo);
            if (converteCampo < 0){
                return false;
            }
            return true;
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Existe um campo com valor incorreto. Por favor, verifique todos os campos.");
            return false;
        }
    }

