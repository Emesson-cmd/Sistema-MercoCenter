            preparo.setString(5, txtUsuEndereco.getText());
            preparo.setString(6, txtBuscaNome.getText());
            preparo.setString(7, txtUsuDataCad.getText());
            preparo.setString(8, txtUsuHoraCad.getText());
            preparo.setString(9, txtUsuNumCasa.getText());
            preparo.setString(10, txtUsuRg.getText());
            preparo.setString(11, txtUsuSexo.getText());
            preparo.setString(12, txtUsuTelefone.getText());
            preparo.setString(13, txtUsuUf.getText());
            preparo.setString(14, txtUsuBairro.getText());
            preparo.setString(15, txtUsuCidade.getText());
            preparo.setString(16, txtUsuCep.getText());
            preparo.setString(17, txtIdUsuario.getText());

