    // método para consultar usuário
    private void consultar() {
        for (int i = 0; i < this.funcionarios.size(); i++) {
            Integer getCodToString = funcionarios.get(i).getCod();
            String stringConvertida = String.valueOf(getCodToString);

            if (txtIdUsuario.getText().equals(stringConvertida)) {

                txtBuscaNome.setText(funcionarios.get(i).getNome());
                txtUsuarioCpf.setText(funcionarios.get(i).getCpf());
                txtUsuDataNasc.setText(funcionarios.get(i).getDatanasc());
                txtUsuEmail.setText(funcionarios.get(i).getEmail());
                txtUsuEndereco.setText(funcionarios.get(i).getEndereco());
                txtUsuDataCad.setText(funcionarios.get(i).getDatacad());
                txtUsuHoraCad.setText(funcionarios.get(i).getHoracad());
                txtUsuNumCasa.setText(funcionarios.get(i).getNume());
                txtUsuRg.setText(funcionarios.get(i).getRg());
                txtUsuSexo.setText(funcionarios.get(i).getSexo());
                txtUsuTelefone.setText(funcionarios.get(i).getTelefone());
                txtUsuUf.setText(funcionarios.get(i).getUf());
                txtUsuBairro.setText(funcionarios.get(i).getBairro());
                txtUsuCidade.setText(funcionarios.get(i).getCidade());
                txtUsuCep.setText(funcionarios.get(i).getCep());
                txtUsuCelular.setText(funcionarios.get(i).getCelular());

            }
        }
    }

    // método para adicionar usuários
    private void adicionar() {
        String sqlFuncionario = "INSERT INTO `funcionario` (`celular`, `cpf`, "
                + "`datanasc`, `email`, `endereco`, `nome`, `datacad`, `horacad`, `nume`, "
                + "`rg`, `sexo`, `telefone`, `uf`, `bairro`, `cidade`, `cep`) "
                + "VALUES "
                + "(?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?);";

        try {

            pst = conexao.prepareStatement(sqlFuncionario);

            pst.setString(1, txtUsuCelular.getText());
            pst.setString(2, txtUsuarioCpf.getText());
            pst.setString(3, txtUsuDataNasc.getText());
            pst.setString(4, txtUsuEmail.getText());
            pst.setString(5, txtUsuEndereco.getText());
            pst.setString(6, txtBuscaNome.getText());
            pst.setString(7, txtUsuDataCad.getText());
            pst.setString(8, txtUsuHoraCad.getText());
            pst.setString(9, txtUsuNumCasa.getText());
            pst.setString(10, txtUsuRg.getText());
            pst.setString(11, txtUsuSexo.getText());
            pst.setString(12, txtUsuTelefone.getText());
            pst.setString(13, txtUsuUf.getText());
            pst.setString(14, txtUsuBairro.getText());
            pst.setString(15, txtUsuCidade.getText());
            pst.setString(16, txtUsuCep.getText());

            // a linha abaixo executa uma atualização do banco de dados com os dados do formulário
            int adicionarEmUsuario = pst.executeUpdate();

            // A linha abaixo serve para ver qual o valor de adicionar após executar executeUpdate
            System.out.println("Valor de adicionar: " + adicionarEmUsuario);

            // a linha abaixo mostra uma confirmação de usuário cadastrado no sistema
            if (adicionarEmUsuario > 0) {
                JOptionPane.showMessageDialog(null, "Funcionário adicionado com sucesso!");
            } else {
                JOptionPane.showMessageDialog(null, "Houve um erro!");

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

